int mlex_read(char* s){

	mlex_char = s;
	int s_idx = 0;
	int c_idx = mlex_input_map[s[s_idx]];
	int end_state, next = start_index, *next_vt = 0;

	while ((c_idx != -1 || mlex_input_map[1] != -1)) {

		//如果是-1，说明存在任意字符的规则
		if (c_idx == -1) {
			c_idx = 0;
		}

		//保存最后一个状态
		end_state = next;

		//获取下一个状态
		next = mlex_state_metrix[next][c_idx];
		if (next == -1) {
			break;
		}

		//保存下一个状态的状态数组
		next_vt = mlex_state_metrix[next];

		c_idx = mlex_input_map[s[++s_idx]];
		if (s[s_idx] == 0){
			break;
		}
	}

	if (!(next_vt != 0 && next_vt[input_count] == 1)){
		return 0;
	}

	mlex_next = s + s_idx;

	switch(end_state){
	case 0:
		{return 9;}
		break;
	case 1:
		{return 6;}
		break;
	case 2:
		{return 7;}
		break;
	case 3:
		{return 24;}
		break;
	case 4:
		{return 16;}
		break;
	case 5:
		{return 23;}
		break;
	case 6:
		{return 13;}
		break;
	}
}
